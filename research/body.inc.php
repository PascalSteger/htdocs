  <h2>
    Research
  </h2>

  Welcome to my astrophysics homepage. I am currently a PhD student at the ETH of Zurich, Switzerland. My main area of research is in dark matter in small galaxies.<br>

  I study dwarf spheroidal galaxies over their whole evolution using simulations, and by constructing mass models of observations to compare to real systems.<br>

  For further information, please see the links below.

